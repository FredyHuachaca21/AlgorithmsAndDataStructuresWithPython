
# O(1) - Constant Time - Esta función tiene una complejidad de O(1) ya que el número de operaciones que realiza es
# "CONSTANTE" y no depende del número de elementos que recibe como parámetro.

# En el siguiente ejemplo, la función add_items tiene una complejidad de O(1) ya que el número de operaciones que
# realiza es constante, independientemente del valor de n.

def add_items(n):
    return n + n + n


print(add_items(1))
